#. Uma pessoa está organizando uma festa e precisa de 1,5 litros de refrigerante para cada convidado. Se
#ela convidou 30 pessoas, quantos litros de refrigerante ela precisará?

#Entrada de dados
convidados = (30)
litros_por_convidado = (1.5)

#Processamento de dados
Litros_para_festa = int(convidados * litros_por_convidado)

#Sáida de dados
print("Vai precisar de:", round(Litros_para_festa, 1), "L de refri para festa") 