#Faça um algoritmo que divide dois numeros e imprima o resultado

#Entrada de dados
numero1 = int(input("digite o valor:"))
numero2 = int(input("digite o valor:"))

#Processamento de dados
resultado = (numero1 // numero2)

#Saída de dados 
print(f"o produto da multiplicação de {numero1} // {numero2} é {resultado}")



