#Um homem percorreu 10km em uma hora e meia. Qual é a sua velocidade média em km/h?

#Entrada de dados
distancia_percorrida = (10)
tempo_de_corrida = (1.5)

#Processamento de dados
velocidade_media = (distancia_percorrida / tempo_de_corrida)

#Sáida de dados
print("A velocidade média foi de:", round(velocidade_media, 3), "km/h ")