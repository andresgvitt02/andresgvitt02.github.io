#Uma escola de inglês oferece um curso de 6 meses que custa R$ 2.400. Qual é o valor mensal do
#curso?

#Entrada de dados
tempo_de_curso = (6)
valor_total = (2400)

#Processmento de dados
valor_mensal = (valor_total / tempo_de_curso)

#Sáida de dados
print("O valor mensal do curso é:", round(valor_mensal, 1), "$") 